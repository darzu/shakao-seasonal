function setSeed () {
    dist = 1000
    for (let value of sprites.allOfKind(SpriteKind.Seeds)) {
        if (Math.sqrt((value.x - friend.x) * (value.x - friend.x) + (value.y - friend.y) * (value.y - friend.y)) < dist) {
            dist = Math.sqrt((value.x - friend.x) * (value.x - friend.x) + (value.y - friend.y) * (value.y - friend.y))
            nearestSeed = value
        }
    }
}
