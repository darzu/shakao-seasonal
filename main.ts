function checkInteractions (loc: number, thing: number) {
    target = locs[loc]
    if (thing == 1) {
        if (target == 3) {
            if (mugContents == 0) {
                mugContents = 3
                friend.say("Cup o' dirt", 1000)
                pause(100)
            } else if (mugContents == 1) {
                if (Math.sqrt((plant.x - lamp.x) * (plant.x - lamp.x) + (plant.y - lamp.y) * (plant.y - lamp.y)) < 20) {
                    if (plantGrowth < 3) {
                        plantGrowth = 3
                        mugContents = 0
                        growPlant()
                        animateGrow()
                    } else if (plantGrowth < 4) {
                        plantGrowth = 4
                        mugContents = 0
                        growPlant()
                        animateGrow()
                    }
                } else {
                    if (plantGrowth < 3) {
                        plantGrowth += 1
                        mugContents = 0
                        growPlant()
                    } else {
                        friend.say("Can't overwater it", 1000)
                    }
                }
                pause(100)
            } else if (mugContents == 2) {
                mugContents = 0
                plantGrowth += -1
                friend.say("Coffee stunts growth :(", 2000)
                growPlant()
                pause(100)
            } else if (mugContents == 3) {
                mugContents = 0
                pause(100)
            }
        } else if (target == 2) {
            mugContents = 2
            friend.say("Cup o' coffee", 1000)
            pause(100)
        }
    } else if (thing == 2) {
        if (target == 3) {
            if (plantGrowth < 3) {
                plantGrowth = 3
                growPlant()
                animateGrow()
            } else if (plantGrowth < 4) {
                plantGrowth = 4
                growPlant()
                animateGrow()
            }
        }
    } else if (thing == 5) {
        plantColor = (plantColor + 1) % 4
        if (plantGrowth < 4) {
            growPlant()
        }
    }
}
function putThing (loc: number, thing: number) {
    if (thing == 1) {
        currentThing = mug
    } else if (thing == 2) {
        currentThing = pot
    } else if (thing == 3) {
        currentThing = plant
    } else if (thing == 4) {
        currentThing = lamp
    } else if (thing == 5) {
        setSeed()
        currentThing = nearestSeed
    }
    if (locs[loc] == 0) {
        if (loc == 0) {
            tiles.placeOnRandomTile(currentThing, myTiles.tile18)
            currentThing.y += -13
        } else if (loc == 1) {
            tiles.placeOnRandomTile(currentThing, myTiles.tile19)
            currentThing.y += -13
        } else if (loc == 2) {
            tiles.placeOnRandomTile(currentThing, myTiles.tile20)
            currentThing.y += -13
        } else if (loc == 3) {
            tiles.placeOnTile(currentThing, tiles.getTileLocation(10, 1))
            currentThing.x += 2
            currentThing.y += 4
        } else if (loc == 4) {
            tiles.placeOnRandomTile(currentThing, myTiles.tile23)
        } else if (loc == 5) {
            tiles.placeOnRandomTile(currentThing, myTiles.tile24)
        } else if (loc == 6) {
            tiles.placeOnTile(currentThing, tiles.getTileLocation(4, 7))
            currentThing.y += 4
        } else if (loc == 7) {
            currentThing.setFlag(SpriteFlag.Invisible, true)
        }
        locs[loc] = holding
        holding = 0
    } else {
        checkInteractions(loc, thing)
    }
}
